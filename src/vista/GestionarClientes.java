    private void TablaProveedoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaProveedoresMouseClicked
        // TODO add your handling code here:
        int clicks = evt.getClickCount();
        int row = TablaProveedores.rowAtPoint(evt.getPoint());
        
        if(clicks==2){
            proveedorAActualizar = proveedorTModel.proveedores.get(row);
            txtNombre.setText(proveedorAActualizar.getNombre());
            txtNit.setText(proveedorAActualizar.getNit());
            
            modo = "actualizar";
        }
    }//GEN-LAST:event_TablaProveedoresMouseClicked

