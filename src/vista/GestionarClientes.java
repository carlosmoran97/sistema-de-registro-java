    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int [] aEliminar = TablaProveedores.getSelectedRows();
        try{
            for(Integer index: aEliminar){
                proveedorBd.eliminarProveedor(proveedorTModel.proveedores.get(index).getId());               
            }
            llenarTabla();
            JOptionPane.showMessageDialog(this, "Registros eliminados correctamente");
        }
        catch(SQLException e){
            javax.swing.JOptionPane.showMessageDialog(this, "Error al eliminar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String nit = txtNit.getText();
        if(nit.isEmpty()){
            llenarTabla();
        }
        else{
            try
            {
                proveedorTModel.proveedores.clear();
                List<Proveedor> result = proveedorBd.obtenerProveedor(nit);
                for(Proveedor prov: result){
                    proveedorTModel.proveedores.add(prov);
                }
                proveedorTModel.fireTableDataChanged();
                TablaProveedores.repaint();
            }
            catch(SQLException e){
                JOptionPane.showMessageDialog(this,"Error en la búsqueda: "+ e.getMessage());
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

