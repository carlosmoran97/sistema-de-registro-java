    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String nit = txtNit.getText();
        String nombre = txtNombre.getText();
        
        if(nombre.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Debe de ingresar un nombre","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        else
        {
            if(nit.isEmpty())
            {
                JOptionPane.showMessageDialog(this, "Debe de ingresar un número de NIT","Error",JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        if("guardar".equals(modo))
        {
            try{
                proveedorBd.insertarProveedor(nombre, nit);
                JOptionPane.showMessageDialog(this, "Se ha insertado el proveedor", "Éxito",JOptionPane.INFORMATION_MESSAGE);
                llenarTabla();
                limpiarFormulario();
            }
            catch(SQLException e)
            {
                JOptionPane.showMessageDialog(this, "Error al insertar el proveedor: " + e.getMessage(), "Error",JOptionPane.ERROR_MESSAGE);
            }
            catch(IllegalArgumentException e){
                JOptionPane.showMessageDialog(this, "Argumento inválido\n" + e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            }
        }
        else if("actualizar".equals(modo)){
            try
            {
                int id = proveedorAActualizar.getId();
                
                
                proveedorBd.actualizarProveedor(id, nombre, nit);
                llenarTabla();
                JOptionPane.showMessageDialog(this, "Proveedor actualizado","Éxito",JOptionPane.INFORMATION_MESSAGE);
                limpiarFormulario();
            }
            catch(SQLException e)
            {
                JOptionPane.showMessageDialog(this,"Error al actualizar: "+ e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed
