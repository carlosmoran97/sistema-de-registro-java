    private void inicializarColumnas(){
        TableColumnModel tColumnModel = new DefaultTableColumnModel();
        
        for (int i = 0; i < proveedorTModel.getColumnCount(); i++) {
            TableColumn col = new TableColumn(i);
            switch(i){
                case 0:
                    col.setHeaderValue("Id");
                    break;
                case 1:
                    col.setHeaderValue("Nombre");
                    break;
                case 2:
                    col.setHeaderValue("NIT");
                    break;
            }
            tColumnModel.addColumn(col);
        }
        TablaProveedores.setColumnModel(tColumnModel);
    }
    private void llenarTabla(){
        proveedorBd.obtenerProveedores();
        proveedorTModel.proveedores.clear();
        for(Proveedor prov: proveedorBd.proveedores){
            proveedorTModel.proveedores.add(prov);
        }
        proveedorTModel.fireTableDataChanged();
        TablaProveedores.repaint();
    }
