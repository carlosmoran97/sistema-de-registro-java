    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText();
        String descripcion = txtDescripcion.getText();
        String unidadDeMedida = txtUnidadDeMedida.getText();
        String codigo = txtCodigo.getText();
        float precioSugerido = Float.parseFloat(txtPrecioSugerido.getText());
        if(nombre.isEmpty() 
                || descripcion.isEmpty() 
                || unidadDeMedida.isEmpty()
                || codigo.isEmpty()
        )
        {
            JOptionPane.showMessageDialog(this, "Debe de introducir todos los campos.","Advertencia",JOptionPane.WARNING_MESSAGE);
            return;
        }
        try
        {
            if(modo.equals("guardar"))
            {
                productoBd.insertarProducto(nombre, descripcion, unidadDeMedida, codigo, precioSugerido);
                llenarTabla();
                JOptionPane.showMessageDialog(this, "Producto guardado correctamente","Información",JOptionPane.INFORMATION_MESSAGE);
            }
            else if(modo.equals("actualizar"))
            {
                
            }
        }
        catch(IllegalArgumentException e)
        {
            JOptionPane.showMessageDialog(this,"Argumento inválido:\n"+ e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(this,"Error al guardar el producto:\n" + e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

