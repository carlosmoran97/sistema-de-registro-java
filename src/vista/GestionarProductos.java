    private void inicializarColumnas()
    {
        TableColumnModel tColumnModel = new DefaultTableColumnModel();
        for (int i = 0; i < 5; i++) {
            TableColumn col = new TableColumn(i);
            switch(i)
            {
                case 0:
                    col.setHeaderValue("Id del prodcuto");
                    break;
                case 1:
                    col.setHeaderValue("Nombre");
                    break;
                case 2:
                    col.setHeaderValue("Descripción");
                    break;
                case 3:
                    col.setHeaderValue("Unidad de medida");
                    break;
                case 4:
                    col.setHeaderValue("Código");
                    break;
                case 5:
                    col.setHeaderValue("Precio sugerido");
                    break;
            }
            tColumnModel.addColumn(col);
        }
        tablaProductos.setColumnModel(tColumnModel);
    }
    private void llenarTabla()
    {
        try{
            productoBd.obtenerProductos();
            productoTModel.productos.clear();
            for(Producto producto: productoBd.productos)
            {
                productoTModel.productos.add(producto);
            }
            productoTModel.fireTableDataChanged();
            tablaProductos.repaint();
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(this, "Error al obtener los productos:\n"+e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        }
    }
